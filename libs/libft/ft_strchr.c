#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	trgt;

	i = 0;
	trgt = (char)c;
	while (s[i] != '\0')
	{
		if (s[i] == trgt)
			return ((char*)(&s[i]));
		i++;
	}
	return (trgt == '\0' ? (char*)(&s[i]) : 0);
}
